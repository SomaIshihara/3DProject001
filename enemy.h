#ifndef _ENEMY_H_
#define _ENEMY_H_

#include "main.h"
#include "model.h"

//マクロ
#define MAX_ENEMY	(64)	//敵総数

//敵構造体
typedef struct
{
	D3DXVECTOR3 pos;		//位置
	D3DXVECTOR3 posOld;
	float fLength;
	float fAngle;
	D3DXVECTOR3 move;		//移動量
	D3DXVECTOR3 rot;		//向き
	Model *pModel;			//使用モデルポインタ
	int nNumModel;			//パーツ総数
	D3DXMATRIX mtxWorld;	//ワールドマトリ
	int nIdxShadow;			//影番号
} Enemy;

//プロトタイプ宣言
void InitEnemy(void);
void UninitEnemy(void);
void UpdateEnemy(void);
void DrawEnemy(void);
Enemy *GetEnemy(void);

#endif // !_ENEMY_H_
